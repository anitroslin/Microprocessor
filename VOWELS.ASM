DATA SEGMENT
STRING DB "PAUL $"
B DB 1
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX,DATA
MOV DS,AX
MOV SI,OFFSET STRING
MOV BX,00

LOOP1:
MOV AH,[SI]
CMP AH,'$'
JE SKIP
CMP AH,'A'
JE M1
CMP AH,'E'
JE M1
CMP AH,'I'
JE M1
CMP AH,'O'
JE M1
CMP AH,'U'
JE M1
INC SI
JMP LOOP1

M1:
INC BX
MOV DX,BX
INC SI
JMP LOOP1

SKIP:
ADD DX,48
MOV AH,02H
INT 21H

MOV AH,4CH
INT 21H
CODE ENDS
END START
