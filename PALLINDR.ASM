ASSUME CS:CODE, DS:DATA
DATA SEGMENT
STRING DB "ANIT$"
S1 DB "PALLINDROME$"
S2 DB "NOT A PALLINDROME$"
DATA ENDS;

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
MOV SI, OFFSET STRING

LOOP1:
MOV AH,[SI]
CMP AH,'$'
JE L1
INC SI
JMP LOOP1

L1:
DEC SI
MOV DI,OFFSET STRING

LOOP2:
MOV AH,[DI]
CMP AH,'$'
JE L2
MOV AL,[SI]
CMP AH,AL
JNE L3
INC DI
DEC SI
JMP LOOP2

L2:
LEA DX,S1
JMP L4

L3:
LEA DX,S2

L4:
MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H

CODE ENDS
END START;                         1


